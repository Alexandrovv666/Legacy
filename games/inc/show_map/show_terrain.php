<?
function function_show_terrain($x_terrain){
            if ($x_terrain == 0)
                return 'terrain-ozero';
            if ($x_terrain == 1)
                return 'terrain-trawa';
            if ($x_terrain == 2)
                return 'terrain-t_zemla';
            if ($x_terrain == 3)
                return 'terrain-wald';
            if ($x_terrain == 4)
                return 'terrain-berg';
            if ($x_terrain == 5)
                return 'terrain-pust';
            if ($x_terrain == 6)
                return 'terrain-step';
            if ($x_terrain == 7)
                return 'terrain-sw';
}
?>
